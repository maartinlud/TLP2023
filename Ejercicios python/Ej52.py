oracion = input("Ingrese una oración: ")

espacios_blancos = oracion.count(" ")
print("Cantidad de espacios en blanco:", espacios_blancos)

oracion_mayusculas = oracion.upper()
print("Oración en mayúsculas:", oracion_mayusculas)

oracion_minusculas = oracion.lower()
print("Oración en minúsculas:", oracion_minusculas)

oracion_capitalizada = oracion.capitalize()
print("Oración con letra capital:", oracion_capitalizada)